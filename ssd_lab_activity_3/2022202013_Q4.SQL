CREATE TABLE M SELECT * FROM DEPENDENT WHERE SEX="F";
CREATE TABLE N SELECT ESSN , COUNT(*) FROM M GROUP BY ESSN;
CREATE TABLE P SELECT DNUMBER FROM DEPARTMENT WHERE MGR_SSN IN (SELECT ESSN FROM N);
CREATE TABLE Q SELECT * FROM DEPT_LOCATIONS WHERE DNUMBER IN (SELECT * FROM P);
CREATE TABLE R SELECT DNAME , Q.DNUMBER FROM Q, DEPARTMENT;
CREATE TABLE S SELECT DNAME ,COUNT(*) AS C FROM R;